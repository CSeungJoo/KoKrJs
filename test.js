  console.log("연도가져오기:", 내날짜.연도가져오기());
  console.log("UTC연도가져오기:", 내날짜.UTC연도가져오기());
  console.log("월가져오기:", 내날짜.월가져오기());
  console.log("UTC월가져오기:", 내날짜.UTC월가져오기());
  console.log("일가져오기:", 내날짜.일가져오기());
  console.log("UTC일가져오기:", 내날짜.UTC일가져오기());
  console.log("요일가져오기:", 내날짜.요일가져오기());
  console.log("UTC요일가져오기:", 내날짜.UTC요일가져오기());

  // 시간 관련 데이터
