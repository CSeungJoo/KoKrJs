  console.log("UTC 일 설정 후:", 내날짜2.문자열로());

  // UTC 월 설정 테스트
  내날짜2.UTC월설정하기(0); // 1월
  console.log("UTC 월 설정 후:", 내날짜2.문자열로());

  // UTC 연도 설정 테스트
