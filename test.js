  console.log("UTC시간가져오기:", 내날짜.UTC시간가져오기());
  console.log("분가져오기:", 내날짜.분가져오기());
  console.log("UTC분가져오기:", 내날짜.UTC분가져오기());
  console.log("초가져오기:", 내날짜.초가져오기());
  console.log("UTC초가져오기:", 내날짜.UTC초가져오기());
  console.log("밀리초가져오기:", 내날짜.밀리초가져오기());
  console.log("UTC밀리초가져오기:", 내날짜.UTC밀리초가져오기());

