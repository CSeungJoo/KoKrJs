  // 밀리초 설정 테스트
  내날짜2.밀리초설정하기(123);
  console.log("밀리초 설정 후:", 내날짜2.문자열로());

  // 초 설정 테스트 (현지 시간)
  내날짜2.초설정하기(45, 500);
  console.log("초 설정 후 (현지 시간):", 내날짜2.문자열로());

  // 분 설정 테스트 (현지 시간)
  내날짜2.분설정하기(30, 45, 300);
  console.log("분 설정 후 (현지 시간):", 내날짜2.문자열로());

  // 시간 설정 테스트 (현지 시간)
  내날짜2.시간설정하기(13, 30, 45, 250);
  console.log("시간 설정 후 (현지 시간):", 내날짜2.문자열로());

  // 일 설정 테스트 (현지 시간)
  내날짜2.일설정하기(15);
  console.log("일 설정 후 (현지 시간):", 내날짜2.문자열로());

  // 월 설정 테스트 (현지 시간)
